package model;

public class Winds {
    private float speed;
    private float degree;

    public float getSpeed() {
        return speed;
    }

    public void setSpeed(float speed) {
        this.speed = speed;
    }

    public float getDegree() {
        return degree;
    }

    public void setDegree(float degree) {
        this.degree = degree;
    }

    public float setSpeed() {
        return speed;
    }
}
